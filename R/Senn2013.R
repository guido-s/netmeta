#' preprocessed for use in R package netmeta.
#' 
#' @name Senn2013
#' 
#' @docType data
#' 
#' @details
#' Treatment labels provided by columns \code{treat1} and
#' \code{treat2} have been abbreviated:
#' \itemize{
#' \item acar = Acarbose
#' \item benf = Benfluorex
#' \item metf = Metformin
#' \item migl = Miglitol
#' \item piog = Pioglitazone
